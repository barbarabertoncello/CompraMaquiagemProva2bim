    private void jBtnExcluir_bbdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluir_bbdActionPerformed
        // TODO add your handling code here:
        if (Util.perguntar("Deseja excluir?")) {
            if (jTable1.getSelectedRow() == -1) {
                Util.mensagem("Selecione uma linha antes");
            } else {
                fornecedorDao_bbd.delete(fornecedorControle.getBean(jTable1.getSelectedRow()));
                fornecedorControle.removeList(jTable1.getSelectedRow());
                Util.mensagem("Excluido");
            }
        } else {
            Util.mensagem("Exclus√£o cancelada");
        }
    }//GEN-LAST:event_jBtnExcluir_bbdActionPerformed

    private void jBtnAlterar_bbdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterar_bbdActionPerformed
        // TODO add your handling code here:

          if (jTable1.getSelectedRow() == -1) {
            Util.mensagem("Selecione uma linha Para alterar!");
        } else {
            JDlgFornecedorIA jDlgFornecedor = new JDlgFornecedorIA(null, true);
            jDlgFornecedor.setTelaAnterio(this);
            jDlgFornecedor.setTitle("Alterar");
            jDlgFornecedor.ativar();
            jDlgFornecedor.beanView(fornecedorControle.getBean(jTable1.getSelectedRow()));
            jDlgFornecedor.setVisible(true);
        }
    }//GEN-LAST:event_jBtnAlterar_bbdActionPerformed

